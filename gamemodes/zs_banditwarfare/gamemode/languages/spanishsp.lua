<p><h3>Pistolas</h3><p>
<p>Las pistolas se dividen en tres grandes categorías: «Rápidas», «Precisas» y «Divisibles».</p>
<p>Las <b>pistolas rápidas</b> tienen mucha cadencia de tiro pero suelen hacer poco daño. Algunas, como la <i>Terminator</i> o la <i>Borradora</i>, pueden ser potentes, pero no conservan ese daño consistente. Como norma general, estas armas son buenas como respaldo.</p>
<p>Las <b>pistolas precisas</b> ofrecen potencia pero sacrifican la velocidad de disparo. Son muy precisas y se pueden usar a larga distancia, y son buena opción para llevar como secundaria junto con una escopeta.</p>
<p>Las <b>pistolas divisibles</b> son las que disparan varias balas a la vez. Esto, técnicamente, reduce la precisión, pero amplía el efecto de alcance del disparo.</p> 
<p>Las <b>divisibles</b> y <b>precisas</b> convergen en la <i>Ventiladora</i>, que ofrece ambas características. Tiene mucha potencia de disparo por bala y buena precisión y además puede lanzar todo el cargador de un disparo si fuera necesario.</p>
<hr>
<p><h3>Escopetas</h3><p>
<p>Las <b>escopetas</b> lanzan una amplia cantidad de balas en cada disparo. Cada bala, por separado, causa poco daño, pero a corto alcance, el daño de todas las balas puede ser letal.</p>
<p>Las escopetas se dividen en las que causan mucho daño pero disparan lento (<i>Ruptura</i>) y las automáticas, que disparan rápido pero causan menos daño (<i>Albatros</i>).</p>
<hr>
<p><h3>Fusiles de asalto</h3><p>
<p>Los <b>fusiles de asalto</b> son armas automáticas con diversos niveles de precisión. Se diseñan para usarse de forma estacionaria y la mayoría de ellos no tienen precisión en movimiento. Se caracterizan por ser equilibrados. Algunos de los más avanzados cuentan con mirillas.</p>
<hr>
<p><h3>Metralletas (SMG)</h3><p>
<p>Las <b>metralletas</b> son la contraparte del fusil de asalto, con una velocidad de disparo mayor pero menos precisión. A diferencia de los fusiles de asalto, algunas de ellas están diseñadas para usarse en movimiento. Aunque las metralletas hacen menos daño, la velocidad de disparo es mayor a la de los fusiles.</p>
<p>Algunas de las más avanzadas están más equilibradas, perdiendo alguna ventaja pero mitigando inconvenientes (<i>Segadora</i>).</p>
<hr>
<p><h3>Rifles</h3><p>
<p>Los <b>rifles</b> se construyeron para enfrentamientos a largas distancias, con una capacidad de daño inmensa, pero una velocida de disparo nula. La mayoría de ellos están equipados con una mirilla de francotirador que ayuda al apuntado. La precisión es la clave de estas armas.</p>
<p>Algunos de estos rifles están modificados para disparar más rápido, causando algo menos de daño por disparo. Esto es más apropiado en casos en los que un disparo limpio no es viable.</p>
<hr>
<p><h3>Ballestas</h3><p>
<p>Las <b>ballestas</b> son las armas de proyectiles principales. Dependiendo de la ballesta utilizada, estos proyectiles tendrán más o menos capacidad de rebotar en superficies, empalar a varios objetivos o causar hemorragia. Las ballestas suelen ser complicadas de categorizar como tipo de arma. Como consecuencia, pueden atravesar campos de fuerza con sus proyectiles. Sin embargo, las medidas de mitigación de proyectiles, como los interceptores, pueden evitarlos.</p>
<hr>
<p><h3>Bioarmas</h3><p>
<p>Las <b>bioarmas</b> son vástagos de ballestas con distintas características. Los proyectiles disparados por estas armas están fabricados de biotecnología zombi reutilizada y tienen las mismas características que el veneno de los zombis. Esto convertirá a los enemigos en carne que puede recogerse como munición, causando mucho daño. Esto se debe a que los daños por veneno de zombi se regeneran rápidamente cuando pasa el efecto.</p>
<p>Como la mayoría de bioarmas están construidas para disparar varios proyectiles en un breve periodo de tiempo, pueden sortear las medidas antiproyectiles.</p>
<hr>
<p><h3>Armas de pulsos</h3><p>
<p>A pesar de que las <b>armas de pulsos</b> comparten el mismo tipo de munición, son muy distintas unas de otras. El verdadero potencial de las armas de pulsos es que se pueden mejorar para obtener su mejor versión. Las armas de pulsos pueden parecer encajar en otras categorías de armas. Sin embargo, pueden ser mejoradas a una inmensa variedad de armas de pulsos distintas, permitiendo cambiar mucho más rápido entre los tipos de armas de otras categorías.</p>
<p>Hay distintos puntos en los que acceder a la línea de armas de pulsos, desde distintos tipos de armas diversas. Puede ser buena opción experimentar usando armas de pulso entre distintos tipos de armas.</p>
<hr>
<p><h3>Armas médicas</h3><p>
<p>Las <b>armas médicas</b> tienen dos propósitos: curar a los aliados y perjudicar a los enemigos. Normalmente esto sucede aplicando una sobredosis al enemigo que le quita salud con el paso del tiempo. Algunas armas médicas lanzan proyectiles, mientras otras solo disparan balas normales. Sin embargo, todas tienen doble función de curar a los aliados y dañar a los enemigos.</p>
<p>Las armas médicas, como los botiquines, pueden usarse para hacer desaparecer los efectos de estado de aliados o del propio jugador, cuando la cantidad de salud recuperada sea la suficiente.</p>
