<p>권총은 크게 세 부류(연발식, 정밀식, 산탄식)로 나눌 수 있습니다.</p>
<p><b>연발식 권총</b>은 높은 발사 속도를 자랑하지만 통상적으로 한발이 입히는 피해는 약합니다. 이 중 몇몇 (<i>터미네이터</i>, <i>이레이서</i> 등) 무기는 상당히 높은 피해를 입힐 수 있는 특성을 가지고 있지만 상황에 따라 다르기 때문에 계속 높은 피해를 기대하기 힘들 수 있습니다. 이 권총류는 다른 더 강력한 무기를 사용한다 하더라도 비상대책으로 가지고 있을 만한 성능을 냅니다.</p>
<p><b>정밀식 권총</b>은 더 높은 피해를 입힐 수 있지만 발사 속도는 연발식에 비해 낮습니다. 이 권총들은 매우 정확하게 설계되어 장거리 교전에서 유용하게 사용할 수 있으므로, 산탄총 등의 근거리 무기와 같이 사용하면 좋습니다.</p>
<p><b>산탄식 권총</b>은 총알 한 발을 여러 발로 산탄하여 발사하므로 전체적인 정확도는 낮아지지만 더 넓은 영역에 발사할 수 있게 됩니다.</p>
<p><b>산탄식</b>과 <b>정밀식</b> 권총들은 모두 <i>벤틸레이터</i> 권총으로 강화시킬 수 있습니다. 이 권총은 정밀식과 산탄식 두 부류 모두의 특성을 지니고 있습니다. 장거리에서 높은 정확도로 강력한 사격이 가능할 뿐만 아니라 필요 시 탄창 전체를 산탄하여 분사할 수 있도록 설계되었습니다.</p>
<hr>
<p><h3>산탄총</h3><p>
<p><b>산탄총</b>은 많은 양의 탄환을 넓게 발사합니다. 각 탄환 하나하나는 낮은 피해를 입히지만 넓게 많은 탄환이 발사되므로 근거리에서 특히나 파괴적입니다.</p>
<p>산탄총은 <i>세브란스</i>와 같이 무시무시한 파괴력을 자랑하지만 발사 속도는 느린 부류와 자동 격발이 가능해 높은 속도로 산탄을 발사하는 <i>알바트로스</i>와 비슷한 부류로 나눌 수도 있습니다.</p>
<hr>
<p><h3>돌격소총</h3><p>
<p><b>돌격소총</b>은 비교적 높은 정확도로 자동 격발이 가능한 총기입니다. 움직이지 않고 사격할 때 가장 높은 정확도를 가지며, 움직이며 사격할 시 대부분은 정확도가 크게 낮아집니다. 돌격소총 대부분은 비교적 높은 연사 속도가 특징이며 총알 당 입히는 피해 또한 상당합니다. 후반 업그레이드 돌격소총은 조준경 또한 장착되어 있는 경우가 많습니다.</p>
<hr>
<p><h3>기관단총(SMG)</h3><p>
<p><b>SMG</b>는 돌격소총에 비해 연사속도가 더욱 높지만 정확도는 매우 낮습니다. 단, 돌격소총과는 다르게 기관단총은 움직이며 사격할 때도 정확도가 크게 변하지 않도록 설계되었습니다. 총알 당 피해량은 낮지만 높은 발사 속도 때문에, 시간 당 입힐 수 있는 최대 피해 면에서는 대부분의 기관단총이 앞섭니다.</p>
<p>후반 업그레이드로 얻을 수 있는 일부 기관단총(<i>리퍼</i> 등)의 경우 기관단총 특유의 단점을 보완하며 장점 또한 일부 잃는 식으로 균형 잡힌 개조가 이루어져 있습니다.</p>
<hr>
<p><h3>소총</h3><p>
<p><b>소총</b> 은 특유의 높은 피해량과 낮은 연발 속도로 장거리 교전에 특화된 무기들입니다. 대부분은 고배율 조준경이 장착되어 있으며 사용 시 정밀한 조준이 가능합니다. 소총 사용자에게는 정확한 조준만큼 중요한 게 없습니다.</p>
<p>소총 일부는 더 빠르게 발사할 수 있도록 자동 격발이 가능하게 개조되었으며 이들은 총알 당 입히는 피해가 비교적 낮습니다. 이 소총들은 정확한 사격이 힘든 경우에 더 유용합니다.</p>
<hr>
<p><h3>석궁</h3><p>
<p><b>석궁</b>과 석궁 화살을 사용하는 무기는 말 그대로 총알이 아닌 투사체를 발사하는 무기입니다. 사용하는 무기에 따라 이 투사체는 표면에 닿으면 튕겨난다든가, 여러 적을 관통한다든가, 출혈 피해를 입힌다든가 등의 특수한 성능을 띄기도 합니다. 이런 이유로 석궁은 어떤 형식의 무기라고 딱 단정짓기 힘든 종류의 무기입니다. 투사체는 방어막을 통과하므로 적이 방어막을 운용하고 있는 경우에는 석궁을 사용하는 게 좋을 수도 있습니다. 단, 투사체를 상대하기 위한 국지방어기 등의 도구도 있으니 주의해야 합니다.</p>
<hr>
<p><h3>생체무기</h3><p>
<p><b>생체 무기</b>들은 석궁과 비슷하게 투사체를 발사하지만, 고유의 특이한 점들이 있습니다. 생체 무기가 발사하는 투사체는 모두 좀비를 재가공한 탄환들로 좀비의 독과 같은 특성을 지닙니다. 즉, 독 피해로 사망하는 적은 살점 덩어리를 떨어뜨리며 이 살점을 이용해 생체무기를 재장전하게 됩니다. 또한, 좀비 독은 빠르게 회복되므로 이 무기들은 한 번에 큰 피해를 입히고 서서히 적의 체력이 원상복귀되는 특성이 있습니다.</p>
<p>상체 무기 대부분은 많은 양의 독성 투사체를 빠르게 발사하기 때문에, 투사체를 방지하기 위한 국지방어기 등을 양으로 밀어붙여 소모시키기에 유용합니다.</p>
<hr>
<p><h3>펄스탄 무기</h3><p>
<p><b>펄스탄 무기</b>는 모두 같은 펄스 에너지를 사용하지만, 무기 하나하나가 모두 다른 특성과 형태를 취합니다. 펄스 무기의 진정한 장점은 업그레이드 시스템과 같이 연동하여 사용할 수 있다는 점입니다. 펄스 무기 하나하나는 언뜻 보기에는 위에 나열된 무기 종류에 포함되지만 펄스탄을 사용한다는 점 만으로 다른 펄스탄 무기로 강화할 수 있습니다. 이 덕분에 펄스탄 무기를 이용하면 다른 무기 종류로 강화하거나 바꿀 때보다 빠르게 여러 종류의 무기를 사용할 수 있습니다.</p>
<p>펄스탄 무기로 강화할 수 있는 다른 종류의 무기가 많으므로 펄스탄 무기들로 환불하거나 강화해 주로 사용하는 무기 종류를 바꾸는 것 또한 가능합니다.</p>
<hr>
<p><h3>의료용 총기</h3><p>
<p><b>의료용 총기</b>는 치료와 공격 목적 모두를 위해 만들어진 무기들로, 아군을 치료하고 적군에게 피해를 입히는 것 모두 가능합니다. 보통은 의료에너지를 이용해 적군에게 약물 중독 피해를 입히거나 아군을 치료하는 형식으로 사용하며 이 때 적군은 지속적인 중독 피해를 입게 됩니다. 일부 의료용 총기는 투사체를 발사하지만 총알 형식으로 발사하는 무기도 존재합니다. 물론, 아군 치료 목적으로 사용할 수 있는 것은 동일합니다.</p>
<p>의료용 총기는 치료량이 충분하다면 메디킷과 비슷하게 아군의 상태 이상을 해제시키는 데 사용할 수도 있습니다.</p>
